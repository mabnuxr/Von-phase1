/**
 * Get pipeline data for the Pipeline Overview report
 */
export const getPipelineData = () => {
  return opportunities
    .filter((opp) => opp.stage !== 'Closed Won' && opp.stage !== 'Closed Lost')
    .map((opp) => ({
      id: opp.id,
      name: opp.name,
      accountName: opp.accountName,
      owner: opp.owner,
      stage: opp.stage,
      amount: opp.amount,
      closeDate: opp.closeDate,
      probability: opp.probability / 100,
      dealRisk: opp.dealRisk,
    }));
};

/**
 * Get at-risk deals data
 */
export const getDealsAtRiskData = () => {
  const riskReasons: Record<string, string> = {
    'opp-005': 'No champion identified, competitor engaged',
    'opp-010': 'Stalled engagement, multiple stakeholders unengaged',
    'opp-003': 'Long sales cycle, budget not confirmed',
  };

  return opportunities
    .filter((opp) => opp.dealRisk === 'High' || opp.dealRisk === 'Medium')
    .filter((opp) => opp.stage !== 'Closed Won' && opp.stage !== 'Closed Lost')
    .map((opp) => ({
      id: opp.id,
      name: opp.name,
      accountName: opp.accountName,
      owner: opp.owner,
      amount: opp.amount,
      closeDate: opp.closeDate,
      dealRisk: opp.dealRisk,
      riskReason: riskReasons[opp.id] || 'Multiple risk factors detected',
      nextStep: opp.nextStep,
    }));
};

/**
 * Get account health data
 */
export const getAccountHealthData = () => {
  const churnRiskMap: Record<number, string> = {};
  accounts.forEach((acc) => {
    if (acc.healthScore < 50) churnRiskMap[acc.healthScore] = 'High';
    else if (acc.healthScore < 70) churnRiskMap[acc.healthScore] = 'Medium';
    else churnRiskMap[acc.healthScore] = 'Low';
  });

  return accounts.map((acc) => ({
    id: acc.id,
    name: acc.name,
    tier: acc.tier,
    csm: acc.csm,
    healthScore: acc.healthScore,
    contractValue: acc.contractValue,
    renewalDate: acc.renewalDate,
    lastEngagement: acc.lastEngagement,
    churnRisk: acc.healthScore < 50 ? 'High' : acc.healthScore < 70 ? 'Medium' : 'Low',
  }));
};

/**
 * Get renewals at risk data
 */
export const getRenewalsAtRiskData = () => {
  const today = new Date('2026-01-13');
  const recommendedActions: Record<string, string> = {
    'acc-004': 'Schedule executive business review immediately',
    'acc-008': 'Re-engage with new value proposition',
    'acc-006': 'Increase touchpoint frequency',
  };

  return accounts
    .filter((acc) => acc.healthScore < 80)
    .map((acc) => {
      const renewalDate = new Date(acc.renewalDate);
      const daysToRenewal = Math.ceil(
        (renewalDate.getTime() - today.getTime()) / (1000 * 60 * 60 * 24)
      );

      return {
        id: acc.id,
        name: acc.name,
        contractValue: acc.contractValue,
        renewalDate: acc.renewalDate,
        healthScore: acc.healthScore,
        daysToRenewal,
        churnRisk: acc.healthScore < 50 ? 'High' : acc.healthScore < 70 ? 'Medium' : 'Low',
        recommendedAction: recommendedActions[acc.id] || 'Proactive outreach recommended',
      };
    })
    .sort((a, b) => a.daysToRenewal - b.daysToRenewal);
};

/**
 * Get rep performance data
 */
export const getRepPerformanceData = () => {
  return salesReps.map((rep) => {
    const repOpps = opportunities.filter((opp) => opp.owner === rep.name);
    const closedWon = repOpps
      .filter((opp) => opp.stage === 'Closed Won')
      .reduce((sum, opp) => sum + opp.amount, 0);
    const pipelineValue = repOpps
      .filter((opp) => opp.stage !== 'Closed Won' && opp.stage !== 'Closed Lost')
      .reduce((sum, opp) => sum + opp.amount, 0);
    const wonDeals = repOpps.filter((opp) => opp.stage === 'Closed Won').length;
    const totalDeals = repOpps.filter(
      (opp) => opp.stage === 'Closed Won' || opp.stage === 'Closed Lost'
    ).length;
    const winRate = totalDeals > 0 ? wonDeals / totalDeals : 0;
    const avgDealSize = wonDeals > 0 ? closedWon / wonDeals : 0;
    const attainment = closedWon / rep.quota;

    return {
      id: rep.id,
      name: rep.name,
      region: rep.region,
      closedWon,
      quota: rep.quota,
      attainment,
      pipelineValue,
      avgDealSize,
      winRate,
      forecast: attainment >= 0.8 ? 'On Track' : attainment >= 0.5 ? 'At Risk' : 'Behind',
    };
  });
};

