// ============================================================================
// Types
// ============================================================================

export interface Account {
  id: string;
  name: string;
  industry: string;
  employees: number;
  annualRevenue: number;
  region: string;
  tier: 'Enterprise' | 'Growth' | 'Starter';
  healthScore: number;
  csm: string;
  contractValue: number;
  renewalDate: string;
  lastEngagement: string;
}

export interface Opportunity {
  id: string;
  name: string;
  accountId: string;
  accountName: string;
  owner: string;
  stage: string;
  amount: number;
  closeDate: string;
  probability: number;
  nextStep: string;
  lastActivity: string;
  dealRisk: 'Low' | 'Medium' | 'High';
  competitorPresent: boolean;
  championIdentified: boolean;
}

export interface Activity {
  id: string;
  opportunityId: string;
  type: 'Call' | 'Email' | 'Meeting' | 'Demo';
  date: string;
  subject: string;
  duration?: number;
  sentiment?: 'Positive' | 'Neutral' | 'Negative';
  keyTopics?: string[];
  followUpRequired: boolean;
}

export interface Contact {
  id: string;
  accountId: string;
  name: string;
  title: string;
  email: string;
  phone: string;
  role: 'Decision Maker' | 'Champion' | 'Influencer' | 'User';
  engagementLevel: 'High' | 'Medium' | 'Low';
  lastContact: string;
}

export interface Report {
  id: string;
  name: string;
  description: string;
  columns: ReportColumn[];
  parentId?: string;
}

